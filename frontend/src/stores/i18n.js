// Idiomas: es (base), en, de, zh, fr.
// El idioma elegido cambia (1) la interfaz y (2) el idioma en que responden las IAs
// (se envía una instrucción de idioma en el system prompt, ver core/identity del backend
// vía la cabecera/preferencia que Andromeda adjunta a cada petición).

import { createSignal } from 'solid-js'

export const LANGS = [
  { id: 'es', label: 'Español',  flag: 'ES' },
  { id: 'en', label: 'English',  flag: 'EN' },
  { id: 'de', label: 'Deutsch',  flag: 'DE' },
  { id: 'zh', label: '中文',      flag: 'ZH' },
  { id: 'fr', label: 'Français', flag: 'FR' },
]

// Nombre del idioma para instruir a las IAs (en su propio idioma destino)
export const LANG_AI_NAME = {
  es: 'español', en: 'English', de: 'Deutsch', zh: '中文 (Chinese)', fr: 'français',
}

const STORAGE_KEY = 'andromeda_lang'
function initialLang() {
  try {
    const saved = localStorage.getItem(STORAGE_KEY)
    if (saved && LANGS.some(l => l.id === saved)) return saved
    const nav = (navigator.language || 'es').slice(0, 2)
    return LANGS.some(l => l.id === nav) ? nav : 'es'
  } catch { return 'es' }
}

export const [lang, setLangSignal] = createSignal(initialLang())

export function setLang(id) {
  if (!LANGS.some(l => l.id === id)) return
  setLangSignal(id)
  try {
    localStorage.setItem(STORAGE_KEY, id)
    document.documentElement.lang = id
    // Persistir también como preferencia para el backend (idioma de las IAs)
    fetch('/api/settings/language', {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({ language: id }),
    }).catch(() => {})
    // Respaldo en el estado de UI persistente (clave para el binario de escritorio)
    fetch('/api/uistate/andromeda_lang', {
      method: 'PUT', headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({ value: id }),
    }).catch(() => {})
  } catch {}
}

/** Rehidrata el idioma desde el backend al arrancar (binario de escritorio). */
export async function hydrateLangFromBackend() {
  try {
    const r = await fetch('/api/uistate/andromeda_lang')
    if (!r.ok) return
    const { value } = await r.json()
    if (value && LANGS.some(l => l.id === value)) {
      setLangSignal(value)
      try { localStorage.setItem(STORAGE_KEY, value) } catch {}
      document.documentElement.lang = value
    }
  } catch {}
}

